// Grabs the model type from sequelize
const { Model, DataTypes } = require('sequelize');
// Grabs sequelize from connection.js
const sequelize = require('../config/connection');

// Makes Post object fit into Sequelize's Model mold
class Post extends Model { }

// Makes the columns to clarify what type of backend data makes up the User MySQL table
Post.init(
    {
        id: {
            type: DataTypes.INTEGER,
            allowNull: false,
            primaryKey: true,
            autoIncrement: true,
        },
        name: {
            type: DataTypes.STRING,
            allowNull: false,
        },
        description: {
            type: DataTypes.STRING,
        },
        date_created: {
            type: DataTypes.DATE,
            allowNull: false,
            defaultValue: DataTypes.NOW,
        },
        user_id: {
            type: DataTypes.INTEGER,
            references: {
                model: 'user',
                key: 'id',
            },
        },
    },
    {
        sequelize,
        timestamps: false,
        freezeTableName: true,
        underscored: true,
        modelName: 'Post',
    }
);

module.exports = Post;